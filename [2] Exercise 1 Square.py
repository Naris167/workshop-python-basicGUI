import turtle

# Set up the screen
screen = turtle.Screen()
screen.title("Turtle Circle Drawing")

rectangle_drawer = turtle.Turtle()
rectangle_drawer.speed(1)

rectangle_drawer.forward(100)
rectangle_drawer.left(90)

rectangle_drawer.forward(100)
rectangle_drawer.left(90)

rectangle_drawer.forward(100)
rectangle_drawer.left(90)
rectangle_drawer.forward(100)
rectangle_drawer.left(90)

turtle.done()